def sentence_bleu(hyp: str, ref: str) -> float:
    try:
        return round(
            sacrebleu.sentence_bleu(
                hyp, [ref],
                smooth_method="exp",
                smooth_value=0.1,
            ).score,
            4,
        )
    except Exception:
        return 0.0


def sentence_chrf(hyp: str, ref: str) -> float:
    try:
        return round(
            sacrebleu.sentence_chrf(
                hyp, [ref],
                char_order=6,
                word_order=2,
                beta=2,
            ).score,
            4,
        )
    except Exception:
        return 0.0


def corpus_bleu(hyps: list, refs: list) -> float:
    try:
        return round(
            sacrebleu.corpus_bleu(
                hyps, [refs],
                smooth_method="exp",
                smooth_value=0.1,
                tokenize="intl",
            ).score,
            4,
        )
    except Exception:
        return 0.0


def corpus_chrf(hyps: list, refs: list) -> float:
    try:
        return round(
            sacrebleu.corpus_chrf(
                hyps, [refs],
                char_order=6,
                word_order=2,
                beta=2,
            ).score,
            4,
        )
    except Exception:
        return 0.0


def compute_bertscore(hyps: list, refs: list) -> tuple:
    """
    Compute BERTScore F1 using bert-base-multilingual-cased.
    Returns (precision%, recall%, f1%) or (0,0,0) if not installed.

    Why multilingual BERT?
    ----------------------
    bert-base-multilingual-cased supports all 11 Indian languages in
    this dataset. It computes token-level cosine similarity in a shared
    embedding space, making it robust to valid paraphrases and
    morphological variation — both common in Indian language NMT output.
    """
    try:
        from bert_score import score as bs
        P, R, F = bs(
            hyps, refs,
            model_type=BERTSCORE_MODEL,
            lang="other",
            idf=True,
            rescale_with_baseline=False,
            batch_size=8,
            verbose=False,
            device="cpu",
        )
        return (
            round(float(P.mean()) * 100, 2),
            round(float(R.mean()) * 100, 2),
            round(float(F.mean()) * 100, 2),
        )
    except ImportError:
        logger.warning("  bert-score not installed → pip install bert-score")
        return 0.0, 0.0, 0.0
    except Exception as exc:
        logger.warning(f"  BERTScore failed: {exc}")
        return 0.0, 0.0, 0.0


def _tokenize(text: str) -> set:
    """
    Space-split for Latin scripts; character-level for Indian scripts
    (Devanagari, Bengali, Tamil, Telugu, Kannada, Malayalam, Gujarati,
    Gurmukhi, Odia) which have Unicode ranges above U+0900.
    """
    if any(ord(c) > 0x0900 for c in text):
        return set(text.strip())   # character-level
    return set(text.lower().split())


def token_metrics(hyps: list, refs: list) -> tuple:
    exact      = []
    precisions = []
    recalls    = []
    f1s        = []

    for hyp, ref in zip(hyps, refs):
        ht = _tokenize(str(hyp))
        rt = _tokenize(str(ref))

        if not rt:
            precisions.append(0.0); recalls.append(0.0); f1s.append(0.0)
            continue

        tp   = len(ht & rt)
        exact.append(1 if hyp.strip() == ref.strip() else 0)
        prec = tp / len(ht) if ht else 0.0
        rec  = tp / len(rt)
        f1   = (2 * prec * rec / (prec + rec)) if (prec + rec) > 0 else 0.0
        precisions.append(prec); recalls.append(rec); f1s.append(f1)
        
    n = len(exact)
    return (
        round(sum(exact)      / n * 100, 2),
        round(sum(precisions) / n * 100, 2),
        round(sum(recalls)    / n * 100, 2),
        round(sum(f1s)        / n * 100, 2),
    )


