def normalize_text(text: str) -> str:
    """
    NFC normalize + strip zero-width chars + collapse whitespace.
    Critical for Indian scripts where the same character can have
    multiple Unicode representations (composed vs decomposed).
    Without this, BERTScore and chrF both undercount matches.
    """
    text = unicodedata.normalize("NFC", str(text))
    text = text.replace("\u200b", "").replace("\ufeff", "").replace("\xa0", " ")
    text = text.replace("\u200c", "").replace("\u200d", "")   # ZWNJ, ZWJ
    text = " ".join(text.split())
    return text.strip()
