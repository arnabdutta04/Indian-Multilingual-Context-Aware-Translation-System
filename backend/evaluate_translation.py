def _bar_chart(
    languages: list,
    values:    list,
    title:     str,
    ylabel:    str,
    color:     str,
    out_path:  Path,
    note:      str = "",
) -> None:
    h = max(5, len(languages) * 0.65 + 1.5)
    fig, ax = plt.subplots(figsize=(11, h))
    bars = ax.barh(languages, values, color=color, edgecolor="white",
                   height=0.55, alpha=0.88)
    for bar, val in zip(bars, values):
        ax.text(bar.get_width() + 0.5,
                bar.get_y() + bar.get_height() / 2,
                f"{val:.2f}", va="center", ha="left",
                fontsize=9, color="#333")
    ax.set_title(title, fontsize=14, fontweight="bold", pad=12)
    ax.set_xlabel(ylabel, fontsize=11)
    max_val = max(values) if values else 100
    ax.set_xlim(0, max_val * 1.18 if max_val > 0 else 100)
    ax.invert_yaxis()
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.grid(axis="x", linestyle="--", alpha=0.35)
    if note:
        fig.text(0.5, 0.01, note, ha="center", fontsize=8,
                 color="#888", style="italic")
    plt.tight_layout(rect=[0, 0.04, 1, 1])
    fig.savefig(out_path, dpi=150)
    plt.close(fig)
    logger.info(f"Graph → {out_path}")


