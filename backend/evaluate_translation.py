def load_csv(path: Path):
    try:
        df = pd.read_csv(path, encoding="utf-8-sig")
    except Exception as exc:
        logger.error(f"Cannot read '{path.name}': {exc}")
        return None

    missing = REQUIRED_COLUMNS - set(df.columns)
    if missing:
        logger.error(f"'{path.name}' missing columns {missing}. Skipping.")
        return None

    df = df.dropna(subset=["input", "ground_truth_translation", "target_language"])
    if df.empty:
        logger.warning(f"'{path.name}' has no usable rows. Skipping.")
        return None

    logger.info(f"  Loaded '{path.name}' — {len(df)} usable rows.")
    return df


