def main() -> None:
    t0 = time.time()

    ensure_directories()
    csv_files = discover_datasets()

    # Warm up backend models before timed evaluation
    logger.info("Warming up backend models…")
    try:
        wr = requests.post(
            BACKEND_URL.replace("/translate", "/warmup"),
            timeout=120,
        )
        logger.info(f"Warmup response: {wr.json()}")
    except Exception as e:
        logger.warning(f"Warmup failed (continuing anyway): {e}")

    all_metrics: list = []
    for csv_path in csv_files:
        df = load_csv(csv_path)
        if df is None:
            continue
        language = str(df["target_language"].iloc[0]).strip()
        metrics  = evaluate_language(df, language)
        if metrics:
            all_metrics.append(metrics)

    if not all_metrics:
        logger.error("No languages evaluated successfully. Exiting.")
        sys.exit(1)

    save_summary(all_metrics)
    generate_graphs(all_metrics)
    print_summary(all_metrics)

    logger.info(f"Complete in {round(time.time()-t0, 1)}s. Results in '{RESULTS_DIR}/'.")


if __name__ == "__main__":
    main()