def evaluate_language(df: pd.DataFrame, language: str) -> dict | None:
    """
    FIX 2: Generate one stable session_id for this entire language
    dataset.  Every row is sent with the same session_id so the
    backend builds up context sentence by sentence — exactly as a
    real user would experience the system.

    FIX 3: Pass emotion from the CSV column to the backend so the
    emotion-conditioning feature (FIX 1 in main_fixed.py) is properly
    exercised and its effect is captured in the metrics.
    """
    lang_start = time.time()
    logger.info(f"\n[START] {language}  ({len(df)} rows)")
    df = df.head(50)
    # ── FIX 2: stable session ID for this language ────────────────────────
    # Format: eval_<language>_<date>_<random>
    # Using a date+random suffix means repeated evaluation runs don't
    # share sessions (which would give the later run an unfair advantage).
    session_id = f"eval_{language.lower()}_{time.strftime('%Y%m%d')}_{uuid.uuid4().hex[:6]}"
    logger.info(f"  Session ID: {session_id}")

    rows_output: list = []
    hyps:  list = []
    refs:  list = []

    for idx, row in df.iterrows():
        raw_input = row.get("input", "")

        # Handle NaN safely
        if pd.isna(raw_input):
            logger.warning(f"  [{language}] NaN input at row {idx} — skipping.")
            continue

        # Convert to clean string
        input_text = str(raw_input)

        # Remove hidden Unicode characters
        input_text = input_text.replace("\u200b", "")
        input_text = input_text.replace("\ufeff", "")
        input_text = input_text.replace("\xa0", " ")

        # Final clean
        input_text = input_text.strip()

        # Strong validation
        if(
            not input_text
            or input_text.lower() in ("nan", "none", "null")
            or len(input_text) < 2
        ):
            logger.warning(f"  [{language}] Invalid input at row {idx} — skipping.")
            continue
        ground_truth = (
            str(row["ground_truth_translation"])
            .replace("\u200b", "")
            .replace("\ufeff", "")
            .replace("\xa0", " ")
            .strip()
        )
        target_lang  = str(row["target_language"]).strip().lower()

        if not ground_truth or ground_truth.lower() in ("nan", "none", "null"):
            logger.warning(f"  [{language}] Empty ground truth at row {idx} — skipping.")
            continue

        # ── Translate with stable session + known emotion ─────────────────
        model_output = translate(
            text        = input_text,
            target_lang = target_lang,
            session_id  = session_id,
            use_context = True,     # context ON — tests the full system
        )
        if idx % 10 == 0:
           logger.info(f"  [{language}] Processed {idx} rows")
        time.sleep(0.25)
        if model_output is None or len(model_output.strip()) < 2:
            logger.warning(f"  [{language}] Row {idx} failed (output='{model_output}') — skipping.")
            continue

        # ── Per-row metrics ───────────────────────────────────────────────
        model_output_norm = normalize_text(model_output)
        ground_truth_norm = normalize_text(ground_truth)

        s_bleu = sentence_bleu(model_output_norm, ground_truth_norm)
        s_chrf = sentence_chrf(model_output_norm, ground_truth_norm)

        rows_output.append({
            "input":        input_text,
            "ground_truth": ground_truth_norm,
            "model_output": model_output_norm,
            "bleu_score":   s_bleu,
            "chrf_score":   s_chrf,
            # BERTScore computed corpus-level only (too slow per-row)
            "bertscore_f1": None,   # filled after corpus computation
        })
        hyps.append(model_output_norm)
        refs.append(ground_truth_norm)

        done = len(rows_output)
        if done % PROGRESS_INTERVAL == 0:
            logger.info(f"  [{language}] {done} rows done…")

    if not rows_output:
        logger.warning(f"  [{language}] Zero rows completed. Skipping.")
        return None

    # ── Corpus-level metrics ──────────────────────────────────────────────
    c_bleu = corpus_bleu(hyps, refs)
    c_chrf = corpus_chrf(hyps, refs)

    logger.info(f"  [{language}] Computing BERTScore (~30s)…")
    bs_p, bs_r, bs_f1 = compute_bertscore(hyps, refs)

    accuracy, precision, recall, f1 = token_metrics(hyps, refs)

    # Fill corpus BERTScore into each row
    for r in rows_output:
        r["bertscore_f1"] = bs_f1

    # ── Console report ────────────────────────────────────────────────────
    logger.info(f"  [{language}] ─────────────────────────────")
    logger.info(f"  [{language}] BLEU      : {c_bleu:>7.2f}")
    logger.info(f"  [{language}] chrF      : {c_chrf:>7.2f}  ← primary")
    logger.info(f"  [{language}] BERTScore : {bs_f1:>7.2f}  ← primary")
    logger.info(f"  [{language}] Accuracy  : {accuracy:>7.2f}%")
    logger.info(f"  [{language}] Token F1  : {f1:>7.2f}%")
    logger.info(f"  [{language}] Session   : {session_id}")

    # ── Save per-language CSV ─────────────────────────────────────────────
    out_df = pd.DataFrame(rows_output)
    # Ensure column order
    col_order = ["input", "ground_truth", "model_output", "bleu_score", "chrf_score", "bertscore_f1"]
    out_df = out_df[[c for c in col_order if c in out_df.columns]]
    out = RESULTS_DIR / f"results_{language.lower()}.csv"
    out_df.to_csv(out, index=False, encoding="utf-8-sig")
    logger.info(f"  [{language}] Saved → {out}")
    logger.info(f"  [{language}] Completed in {round(time.time()-lang_start, 1)}s")
    return {
        "language":       language,
        "accuracy":       accuracy,
        "precision":      precision,
        "recall":         recall,
        "f1_score":       f1,
        "bleu_score":     c_bleu,
        "chrf_score":     c_chrf,
        "bertscore_p":    bs_p,
        "bertscore_r":    bs_r,
        "bertscore_f1":   bs_f1,
        "rows_evaluated": len(rows_output),
        "session_id":     session_id,
        "hyps":           hyps, 
        "refs":           refs,
    }


