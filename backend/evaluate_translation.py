def translate(
    text:        str,
    target_lang: str,
    session_id:  str,
    use_context: bool = True,
) -> str | None:
    text = str(text).replace("\u200b", "").replace("\ufeff", "").replace("\xa0", " ").strip()

    if not text or text.lower() in ("nan", "none", "null") or len(text) < 2:
        logger.warning("  Invalid text before API call — skipping.")
        return None

    payload = {
        "text":        text,
        "source_lang": "english",
        "target_lang": target_lang.lower().strip(),
        "session_id":  session_id,
        "use_context": use_context,
    }

    for attempt in range(3):   # up to 3 attempts
        try:
            resp = requests.post(
                BACKEND_URL,
                json=payload,
                timeout=REQUEST_TIMEOUT,
            )
            resp.raise_for_status()
            data = resp.json()
            break   # success — exit retry loop
        except requests.exceptions.Timeout:
            logger.warning(f"  Timeout attempt {attempt+1} for: '{text[:40]}…'")
            time.sleep(2 ** attempt)   # 1s, 2s, 4s backoff
            data = None
            continue
        except requests.exceptions.ConnectionError:
            logger.error(f"  Cannot reach backend at {BACKEND_URL}.")
            return None
        except requests.exceptions.HTTPError as exc:
            if exc.response.status_code == 429:
                logger.warning(f"  Rate limited — waiting {2**attempt}s…")
                time.sleep(2 ** attempt)
                data = None
                continue
            logger.warning(f"  HTTP {exc.response.status_code}: '{text[:40]}…'")
            return None
        except Exception as exc:
            logger.warning(f"  Unexpected error: {exc}")
            return None
    else:
        logger.warning(f"  All 3 attempts failed for: '{text[:40]}…'")
        return None

    if data is None:
        return None

    # ── DEBUG: print full response on first few calls ──────────────
    logger.debug(
        f"  Full response keys: "
        f"{list(data.keys()) if isinstance(data, dict) else type(data)}"
    )

    if isinstance(data, dict):

            for key in ("translated_text", "translated", "translation", "output", "result"):

                val = data.get(key)

                if val and isinstance(val, str) and val.strip():
                    return " ".join(val.strip().split())

                return None

    elif isinstance(data, str) and len(data.strip()) >= 2:

            return " ".join(data.strip().split())

    return None

